"""Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.
"""

altura = int(input('Digite a altura do quadrado em metros: '))
largura = int(input('Digite a largura do quadrado em metros: '))

area_do_quadrado = altura * largura

print(f'A area do quadro é: {area_do_quadrado} m2, o dobro é {area_do_quadrado * 2} m2.')